def aes_encrypt(plaintext, secret_key):
    # 평문을 4x4 행렬로 만들기
    state = np.array(list(plaintext)).reshape(4, 4).T
    print("=== [초기 State (평문)] ===\n")
    print_state_hex(state)

    # 라운드 키 생성
    round_keys = GetRoundKey(secret_key)

    # 초기 AddRoundKey
    AddRoundKey(state, round_keys[0])
    print("\n=== [0 라운드 후 State] ===")
    print_state_hex(state)

    # 1 ~ 9 라운드
    for r in range(1, 10):
        SubBytes(state)
        ShiftRows(state)
        MixColumns(state)
        AddRoundKey(state, round_keys[r])


    # 10 라운드
    SubBytes(state)
    ShiftRows(state)
    AddRoundKey(state, round_keys[10])

    print("\n=== [10 라운드 (최종) State] ===")
    print_state_hex(state)

    # return state.T.flatten().tobytes()
    # return state.T.flatten().astype(np.uint8).tobytes()
    return bytes(state.T.flatten().tolist())

def print_state_hex(state, title="State"):
    """
    4x4 numpy 배열(state)을 2자리 16진수(대문자) 포맷으로 출력합니다.
    """
    print(f"\n=== [{title}] ===")
    for row in state:
        # 각 요소를 2자리 16진수(02X)로 변환하고 띄어쓰기로 연결
        print(" ".join([f"{val:02X}" for val in row]))
    print("=" * 20)


