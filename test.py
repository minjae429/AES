def GetRoundKey(secret_key):
    aes = pyaes.AES(secret_key)

    # 3. 내부 변수(_Ke)에서 라운드 키 추출
    round_keys_176_bytes = bytearray()

    # pyaes는 총 11라운드(초기 + 10라운드)의 키를 _Ke 배열에 저장합니다.
    # 각 라운드 배열 안에는 4개의 32-bit 정수(Word)가 들어있습니다.
    for round_words in aes._Ke:
        for word in round_words:
            # 32-bit 정수를 4개의 1-byte로 쪼개어 배열에 추가 (Big-Endian 방식)
            round_keys_176_bytes.append((word >> 24) & 0xFF)
            round_keys_176_bytes.append((word >> 16) & 0xFF)
            round_keys_176_bytes.append((word >> 8) & 0xFF)
            round_keys_176_bytes.append(word & 0xFF)

    # 16바이트씩 잘라서 11개의 배열(리스트)로 만들기
    round_keys = [round_keys_176_bytes[i:i + 16] for i in range(0, 176, 16)]

    # 4. 결과 확인
    print(f"추출된 라운드 키의 크기: {len(round_keys_176_bytes)} bytes")
    print(f"Hex 출력: {round_keys_176_bytes.hex()}")

    return round_keys



def AddRoundKey(state, round_key):
    rk_matrix = np.array(list(round_key)).reshape(4, 4).T

    # Numpy의 브로드캐스팅을 이용해 행렬 전체를 한 번에 XOR (for문 불필요)
    state[:] ^= rk_matrix

def SubBytes(state):
    for i in range(4):
        for j in range(4):
            state[i][j] = SBOX[state[i][j]]


def ShiftRows(state):
    state[1] = np.roll(state[1], -1)
    state[2] = np.roll(state[2], -2)
    state[3] = np.roll(state[3], -3)



def MixColumns(state):
    """
    상수 행렬 C와 State 행렬을 GF(2^8) 상에서 행렬 곱셈합니다.
    """
    # 결과를 담아둘 0으로 채워진 빈 4x4 행렬 생성
    temp_state = np.zeros((4, 4), dtype=int)

    # 1. 행렬 곱셈 연산 (3중 for문)
    for i in range(4):  # 상수 행렬 C의 '행'
        for j in range(4):  # State 행렬의 '열'

            # 내적(Dot Product) 계산
            val = 0
            for k in range(4):
                # 곱셈은 gfm() 함수 사용, 덧셈은 누적 XOR(^) 사용
                # 수식: C[i][0]*S[0][j] + C[i][1]*S[1][j] + C[i][2]*S[2][j] + C[i][3]*S[3][j]
                val ^= gfm(C[i][k], state[k][j])

            # 계산된 결과값을 임시 행렬에 저장
            temp_state[i][j] = val

    # 2. 계산이 모두 끝난 후 원본 state에 덮어쓰기
    for i in range(4):
        for j in range(4):
            state[i][j] = temp_state[i][j]


def gfm(a, b):
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a  # b의 현재 비트가 1이면 p에 a를 더함(XOR)
        hi_bit_set = a & 0x80 # a의 최상위 비트(x^7) 확인
        a = (a << 1) & 0xFF   # a를 x 한 번 곱함 (왼쪽 시프트 후 8비트 유지)
        if hi_bit_set:
            # 최상위 비트가 넘어갔다면 기약 다항식(x^8+x^4+x^3+x+1)의 하위 8비트(0x1B)로 나눈 나머지 구함
            a ^= 0x1B
        b >>= 1 # b를 다음 비트로 시프트
    return p

